import java.util.*;

public class patterns1 {
     
    public static void main(String args[]) {
        int n = 4;
        int m = 5;

        for(int i=1; i<=n; i++) {
             
                for(int j=i; j<=m; j++) {
                    System.out.println("*");
                System.out.println();
            }
        }
}
}